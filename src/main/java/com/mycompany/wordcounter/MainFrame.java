    private void ButtonInitProgramActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonInitProgramActionPerformed
        try {
            String selectedValue = (String) ComboWords.getSelectedItem();

            String filename = "";

            // Asigna el nombre del archivo seg√∫n el elemento seleccionado
            if (selectedValue.equals("100")) {
                filename = "C:/Users/xvelazquez/Documents/NetBeansProjects/WordCounter/src/main/java/documents/100w.txt"; // Reemplaza con el nombre de tu archivo
            } else if (selectedValue.equals("1000")) {
                filename = "C:/Users/xvelazquez/Documents/NetBeansProjects/WordCounter/src/main/java/documents/1000w.txt"; // Reemplaza con el nombre de tu archivo
            } else if (selectedValue.equals("10000")) {
                filename = "C:/Users/xvelazquez/Documents/NetBeansProjects/WordCounter/src/main/java/documents/10000w.txt"; // Reemplaza con el nombre de tu archivo
            } else if (selectedValue.equals("100000")) {
                filename = "C:/Users/xvelazquez/Documents/NetBeansProjects/WordCounter/src/main/java/documents/100000w.txt"; // Reemplaza con el nombre de tu archivo
            } else if (selectedValue.equals("1000000")) {
                filename = "C:/Users/xvelazquez/Documents/NetBeansProjects/WordCounter/src/main/java/documents/1000000w.txt"; // Reemplaza con el nombre de tu archivo
            } else if (selectedValue.equals("2000000")) {
                filename = "C:/Users/xvelazquez/Documents/NetBeansProjects/WordCounter/src/main/java/documents/2000000w.txt";
            }

            long startTime = System.currentTimeMillis();
            WordCounterSequential sequentialCounter = new WordCounterSequential(filename);
            int wordCountSequential = sequentialCounter.countWords();
            long endTime = System.currentTimeMillis();
            
            msSeq.setText((endTime - startTime) + " ms");

            startTime = System.currentTimeMillis();
            WordCounterConcurrent concurrentCounter = new WordCounterConcurrent(filename);
            int wordCountConcurrent = concurrentCounter.countWords();
            endTime = System.currentTimeMillis();
            
            msConc.setText((endTime - startTime) + " ms");
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ButtonInitProgramActionPerformed

