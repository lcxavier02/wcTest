    private String getFilename(String selectedValue) {
        String userDir = System.getProperty("user.dir");
        String separator = File.separator;

        filename = userDir + separator + "src" + separator + "main" + separator + 
                "java" + separator + "documents" + separator + selectedValue + "w.txt";

        return filename;
    }

    private int getTotalWords(String filename) throws IOException {
        try (BufferedReader reader = new BufferedReader(new FileReader(filename))) {
            int words = 0;
            String line;
            while ((line = reader.readLine()) != null) {
                String[] lineWords = line.split("\\s+");
                words += lineWords.length;
            }
            return words;
        }
    }
    
    private void ButtonInitProgramActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonInitProgramActionPerformed
        try {
            selectedValue = (String) ComboWords.getSelectedItem();
            filename = getFilename(selectedValue);
            
            int totalFileWords = getTotalWords(filename);
            
            String sepatator = "---------------------------------------\n";
            log.append(sepatator);

            long startTime = System.currentTimeMillis();
            WordCounterSequential sequentialCounter = new WordCounterSequential(filename);
            int wordCountSequential = sequentialCounter.countWords();
            long endTime = System.currentTimeMillis();
            
            msSeq.setText((endTime - startTime) + " ms");
            counterSeq.setText(String.valueOf(wordCountSequential));

            startTime = System.currentTimeMillis();
            WordCounterConcurrent concurrentCounter = new WordCounterConcurrent(filename, log);
            int wordCountConcurrent = concurrentCounter.countWords();
            endTime = System.currentTimeMillis();
            
            msConc.setText((endTime - startTime) + " ms");
            counterConc.setText(String.valueOf(wordCountConcurrent));
            
            
            
            log.getCaret().setDot(Integer.MAX_VALUE);
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ButtonInitProgramActionPerformed

